package java_classes;

public class WhileLoops {
	
	public static void main(String[] args) {
		
		
		int i =0;//declation and init
		
		while(i>100) //expression
		{
			System.out.println(i);
			i++; // update statmen
		}
		
		
		do
		{
			System.out.println(i);
			i++;
		}while(i<100);
		
		
	}

}
